# List vs Tuple

# Lists are mutable, where as tuples are immutable

# mutable - changable/modifyable - list

# immutable - once created we can't modify, only you can re-assign - Eg - Strings/tuple 

lst1 = [10, 20, 30, 40 ,50, 60, 70]

print("before",lst1[3])

lst1[3] = 400

print("after",lst1[3])

